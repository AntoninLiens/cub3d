char    *get_NO(char *line)
{
    char    *dst;
    int     i;
    int     j;

    i = -1;
    j = 0;
    while (line[++i])
    {
        if (line[i] == 'N' || line[i] == 'O' || line[i] == ' ')
            i++;
        dst[j] = line[i];
    }
    return (dst);
}